void main() {
  /**
   * OPERATOR ARITMATIKA
   * Operator               keterangan
   * +                      Pertambahan
   * -, -expression         Pengurangan atau negatif
   * *                      Perkalian
   * /                      Pembagian, hasilnya double
   * ~/                     Pembagian hasilnya int
   * %                      Sisa bagi
   */
  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}

void main() {
  /**
   * WHILE LOOP
   * versi perulangan yang lebih sederhana dibandingkan for loop
   * di while loop, hanya terdapat perulangan, tanpa ada ini statement dan post statement
   */
  var counter = 1;
  while (counter <= 10) {
    print('perulangan while ke-$counter');
    counter++;
  }
}

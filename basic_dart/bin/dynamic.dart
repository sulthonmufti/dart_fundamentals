void main() {
  /**
   * DYNAMIC
   * kadang ada kebutuhan kita ingin membuat sebuah variabel yang bisa
   * menampung semua jinis tipe data.
   * pada kasus ini, kita bisa menggunakan tipe data dynamic.
   */
  dynamic variable = 100;
  print(variable);

  variable = true;
  print(variable);

  variable = 'Mufti';
  print(variable);
}

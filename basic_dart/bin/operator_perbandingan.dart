void main() {
  /**
   * OPERATOR PERBANDINGAN
   * Operator perbandingan adalah operator yang hasilnya bool
   * operator perbandingan bisa digunakan di tipe data number dan string
   * 
   * Operator               keterangan
   * ==                     Sama dengan
   * !=                     Tidak sama dengan
   * >                      Lebih dari
   * <                      Kurang dari
   * >=                     Lebih dari sama dengan
   *<=                      Kurang dari sama dengan
   */
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('Mufti' == 'Sulthon');
  print('Mufti' != 'Mufti');
}
